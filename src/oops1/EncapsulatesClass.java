package oops1;

public class EncapsulatesClass {
//    private variables and public function
    private String name ="suresh ";
    private int age =20;
    private String jpb = "Engineer";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getJpb() {
        return jpb;
    }

    public void setJpb(String jpb) {
        this.jpb = jpb;
    }
}

